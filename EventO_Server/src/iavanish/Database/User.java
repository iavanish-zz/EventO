package iavanish.Database;

public class User {

	public String rollNo;
	public String name;
	public boolean typeOfUser;
	public String emailID;
	
	public User(String rollNo, String name, boolean typeOfUser, String emailID) {
		this.rollNo = rollNo;
		this.name = name;
		this.typeOfUser = typeOfUser;
		this.emailID = emailID;
	}
	
}
